- Güncel tarihli askerlik durum belgesi<br>
- ALES / GMAT / GRE Sonuç Belgesi (Doktora programlarına ve Tezli programlara kayıt olan veya Program tarafından sınav sonuç belgesi istenen adaylar için)<br>
- İngilizce / Dil yeterlik belgesi (Eğitim dili İngilizce olan Tezsiz Yüksek Lisans programları için, Tezli Yüksek  Lisans programları için TOEFL ,YDS, e-YDS  veya BİLET 3   sonucu, Doktora programları için TOEFL, YDS  veya e-YDS sonucu )<br>
- Yabancı Uyruklu Öğrenciler için; Lisans diplomasının noter onaylı fotokopisi (Doktora programları için yüksek lisans diploması)<br>
- YÖK onaylı diploma denklik belgesi, Resmi not dökümü (transkript), GRE ya da GMAT belgesinin aslı (Program’ın talep etmesi halinde), Pasaportun ilgili sayfalarının fotokopisi (kimlik bilgileri)<br>
<br><br>

+ Kayıt için gerekli belgeleri ofisinize gelemediğim durumlarda nasıl ulaştırabilirim?<br>
