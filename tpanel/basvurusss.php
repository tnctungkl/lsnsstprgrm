+ Diploma Eki nedir, nasıl alabilirim?<br>
- Diploma ile birlikte verilen diploma eki; aldığınız dersleri, bu derslerin not karşılığını, mezun olduğunuz programın derecesini, derecenin size hangi statü ve yetkinlikleri kazandırdığını gösteren belgedir. Diplomanızla birlikte teslim edilir.
<br><br>

+ Kredi kartı ile ödemelerde hangi anlaşmalı kartlar geçerlidir?<br>
- İş, Ziraat, Garanti, Halk, Bonus, Shop & Miles, Flexi, Maximum, Cardfinans, Worldcard, Axess, Wings ve Fish, Paysafe, Paypal, Ininal
<br><br>

+ Öğrenci kimliğimi kaybettim. Nasıl yenileyebilirim?<br>
- Lisansüstü Öğrenci İşleri Ofisi ile iletişime geçerek yeni kimlik kartı talebinde bulunabilirsiniz.
<br><br>

+ Talep ettiğim belgeleri ne zaman alabilirim?<br>
- Not dökümünü (transcript) başvuru tarihinden 3 (üç) iş günü sonra, diğer belgeleri ise talep edilen tarihten 1(bir) iş günü sonra öğrenci sayfanızdaki e-imzalı belgelerim kısmından indirebilirsiniz.
<br><br>

+ Derslere devam zorunluluğu var mı?<br>
- Derslere %70 oranında devam zorunluluğu bulunmaktadır.
<br><br>

+ Aldığım bir dersten başarısız olursam, ne yapmalıyım?<br>
- Başarısız olunan ders zorunlu ders ise aynı ders ile seçmeli ders ise aynı ders veya başka bir ders ile bir sonraki akademik yıl için tekrar edilmek zorundadır. Dersin notuna itiraz için dersin öğretim görevlisi ile iletişime geçilmelidir.
<br><br>

+ Lisansüstü kaydı nasıl yapılır?<br>
- Her yarıyıl için akademik takvimde belirtilen tarihler arasında ders kaydı yapmak zorundasınız.<br>
- Lisansütü kayıt işlemi için  “Lisansüstü Başvur” menüsünden; dersleri belirleyip ve seçimlerini yapıp program koordinatörü/danışmanına gerekli şartları sağladıysanız onaylatmanız gerekir. 
<br><br>

+ Lisansüstü kaydıma nasıl onay alabilirim?<br>
-  Lisansüstü kaydı sonlandırıldıktan sonra, sistem tarafından verilen  “Lisansüstü Onay/Red” üzerinden program koordinatörü/danışmana otomatik olarak gönderilmektedir.  Lisansüstü kaydı onaylandıktan sonra ilgili sekmeden bilgi verilmektedir.
<br><br>

+ Farklı bir programdan ders almak için ne yapabilirim?<br>
- Seçmeli ders listesinden seçemediğiniz derse ilişkin dilekçe yazarak dersin öğretim görevlisine onaylatıp Lisansüstü Öğrenci İşleri Ofisi’ne teslim edebilirsiniz.
<br><br>

+ Muafiyet nedir?<br>
- Lisans veya farklı bir yüksek lisans programında aldığınız bir dersin mevcut programda da var olması halinde yapılan başvurudur. Muafiyet başvurusu her yarıyılın ilk haftasında programlara iletilir. Muaf olunan derslerin yerine seçmeli ders almanız gerekmektedir. Muaf olunacak ders sayısı iki ile sınırlıdır.
<br><br>

+ Belirlenen tarihler arasında lisansüstü kaydı yapamadım; ne yapmam gerekiyor?<br>
- Geç ders kayıt talebinde bulunmak üzere ofisimizle iletişime geçebilirsiniz. Mazeretiniz kabul edildiği takdirde ders seçimi yapabilirsiniz.
<br><br>

+ Kayıt dondurma şartları nelerdir?<br>
- Tezli ya da tezsiz programlar için toplam dört yarıyıl süreyle kayıt dondurmak için başvurabilirsiniz. Bir defada ancak iki yarıyıl üst üste kayıt dondurabilirsiniz. Kayıt dondurmak için dönem başladıktan sonraki 7 hafta içerisinde Enstitü Yönetim Kurulu’na kayıt dondurma formu ve kayıt dondurma nedenini gösterir belge ile başvurmanız gerekir.
Başvurunuz kabul edildiğinde onay/red çıkacaktır.
<br><br>

+ Lisansüstü Kayıt sildirme başvurusu nasıl yapılır?<br>
- Enstitü Yönetim Kurulu’na Mali İşler Ofisi tarafından onaylı kayıt sildirme formu ile başvuru yapabilirsiniz.
<br><br>

+ Askerlik tecil işlemleri nasıl yapılır?<br>
- Üniversite tarafından askerlik tecilinin yapılması için kayıt sırasında güncel askerlik durumunu gösterir belge teslim etmeniz gerekmektedir. Son yoklamanın yaptırılmaması, başka bir öğretim kurumunda kayıtlı görünmek gibi nedenler olmadığı sürece tecil işlemleri için olumlu cevap alabilirsiniz.<br>
- Ortalama tecil süresi tezli yüksek lisans öğrencilerimiz için 3 yıl, tezsiz yüksek lisans öğrencilerimiz için 1,5 yıl ve doktora öğrencilerimiz için 6 yıldır. Lisansüstü öğrenciler askerlik durumlarını lisansüstü öğrenci işleri ofisinden veya e-devlet üzerinden öğrenebilirler.
<br><br>

+ Basılı diplomamı benim yerime başka birisi alabilir mi?<br>
- Diploma ancak, noter aracılığıyla vekalet verdiğiniz bir kişi tarafından alınabilir.
<br><br>

+ Diplomamı ne zaman ve nereden alabilirim?<br>
- Diplomalar mezuniyet tarihinden itibaren en geç 2 ay içinde elektronik imzalı olarak hazırlanır. Elektronik imzalı diplomanızı Öğrenci Sayfaları'ndaki e-imzalı belgelerim kısmından indirebilirsiniz.<br>
- Elektronik imzalı diplomanızı mühürlü ve basılı almak istemeniz durumunda öğrenci sayfanız üzerinden belge talebi oluşturmanız gerekmektedir. Basılı diplomanız  2 hafta içinde hazırlanır ve imza karşılığında Lisansüstü Öğrenci İşleri ofisinden teslim edilir.
<br><br>

+ Mezuniyet şartları nelerdir?<br>
- Program’a ait tüm dersleri tamamlayıp, tezi/projeyi teslim ettiğiniz ve 2,80 (doktora programları için 3,20) genel not ortalamasına sahip olduğunuz takdirde mezuniyete hak kazanırsınız.
<br><br>

+ Sınav programımı ne zaman ve nereden görebilirim?<br>
- Sınav programı ve ders programı programlar tarafından duyurulur.</p>
