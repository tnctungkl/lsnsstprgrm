<br><br>

+ Kayıt süreci nasıl ilerler?<br>
- Başvurusu kabul edilen adaylar, evrak teslimi ve ödeme işlemleri için Lisansüstü Öğrenci İşleri Ofisi ve Mali İşler Ofisi ile iletişime geçebilirler.
<br><br>

+Kayıt için gerekli belgeler nelerdir?<br>
- Geçici mezuniyet belgesi, e-devlet mezun belgesi ya da diplomanın noter onaylı fotokopisi (Aslı görülmek suretiyle fotokopisi, aslı gibidir kopyası, noter onaylı örneği; Doktora programları için yüksek lisans diploması ya da geçici mezuniyet belgesi)<br>
