- [ÜNİ BİLGİSİ GİRCEZ]
<br><br>

+ Burs olanakları ile ilgili hangi birim ile görüşmeliyim?<br>
- Burslu kontenjanı olan programlar için program koordinatörü ile iletişime geçebilirsiniz.
<br><br>

+Özel öğrenci olarak belli derslere kayıt yaptırmam mümkün mü?<br>
- Üniversitemizde sunulan bazı derslere, programın uygun görmesi üzerine özel öğrenci olarak kayıt yaptırabilirsiniz. Kayıt yaptırmak için herhangi bir üniversitede Lisansüstü öğrenci olmak koşulu ile program koordinatörü ve ilgili dersin öğretim görevlisinden onay alarak Enstitü Yönetim Kurulu’na dilekçe ile başvurabilirsiniz.<br>
- Dilekçe ekinde güncel öğrenci belgesi ve transkript bulunmalıdır. 
<br><br>

+ Öğrenci İşleri ofisinden hangi belgeleri alabilirim?<br>
- Lisansüstü Öğrenci İşleri’nden disiplin belgesi, öğrenci kimlik kartı, basılı diploma alabilirsiniz.
<br><br>

+ Öğrenci belgesi ve not dökümünü (transcript) nasıl talep edebilirim?<br>
- Öğrenci sayfasında ‘Belge Talep/Document Request’ kısmından not dökümü (transcript) ve öğrenci belgesi talep edebilirsiniz. Belgeler elektronik imza ile hazırlanıp öğrenci sayfasınadaki e-imzalı belgelerim alanına yüklenmekte ve bilgiedu.net uzantılı e-mail adresine gönderilmektedir.
<br><br>

+ Belgelerimi benim yerime başka bir kişi alabilir mi?<br>
- Bu talebinizi lisansustu@tuos.edu.tr adresine e-mail göndererek iletmeniz gerekmektedir.
<br><br>

+ Diplomamı kaybettim, yeni diploma almak için ne yapmalıyım?<br>
- Elektronik imzalı olarak talep edilmesi durumunda lisansustu@bilgi.edu.tr adresine mail ile başvuru yapabilirsiniz.<br>
